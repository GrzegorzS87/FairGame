    AnimationBinary(int[][] data, int width, int height, int duration) {
        this.data = data;
        this.width = width;
        this.height = height;
        this.duration = duration;
    }

    public static AnimationBinary random(){
        AnimationBinary[] type = values();
        int roll;
        do{
            roll = Dice.nextInt(type.length);
        }
        while(roll == 0); // don't want logo to show, and its on pos 0

        return type[roll];
    }

    public static AnimationBinary randomWithout(AnimationBinary notLike){
        AnimationBinary[] type = values();
        int roll;
        do{
            roll = Dice.nextInt(type.length);
        }
        while((roll == 0) && (type[roll] != notLike));

        return type[roll];
    }

    public static AnimationBinary randomWithout(AnimationBinary notLike, AnimationBinary notLike2){
        AnimationBinary[] type = values();
        int roll;
        do{
            roll = Dice.nextInt(type.length);
        }
        while((roll == 0) && (type[roll] != notLike) && (type[roll] != notLike2));

        return type[roll];
    }

    public int[][] getData() {
        return data;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getDuration() {
        return duration;
    }
